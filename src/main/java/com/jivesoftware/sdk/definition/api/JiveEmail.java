package com.jivesoftware.sdk.definition.api;

import java.io.Serializable;

import com.fasterxml.jackson.annotation.JsonProperty;


/**
 * Created by rrutan on 11/10/14.
 */
public class JiveEmail implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1389945975425656213L;

	@JsonProperty("jive_label")
    private String label;

    private boolean primary;

    private String type;

    @JsonProperty("value")
    private String email;

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public boolean isPrimary() {
        return primary;
    }

    public void setPrimary(boolean primary) {
        this.primary = primary;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
