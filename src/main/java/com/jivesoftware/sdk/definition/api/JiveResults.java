package com.jivesoftware.sdk.definition.api;

import java.io.Serializable;
import java.util.List;
import java.util.Map;

/**
 * Created by rrutan on 11/10/14.
 */
public class JiveResults<T> implements Serializable {

    /**
	 * 
	 */
	private static final long serialVersionUID = 1864071374215089051L;
	private int itemsPerPage;
    private Map<String,String> links;
    private List<T> list;
    private int startIndex;

    public String getNext() { return (links != null) ? links.get("next") : null; }
    public String getPrevious() { return (links != null) ? links.get("previous") : null; }

    public int getItemsPerPage() {
        return itemsPerPage;
    }

    public void setItemsPerPage(int itemsPerPage) {
        this.itemsPerPage = itemsPerPage;
    }

    public Map<String, String> getLinks() {
        return links;
    }

    public void setLinks(Map<String, String> links) {
        this.links = links;
    }

    public List<T> getList() {
        return list;
    }

    public void setList(List<T> list) {
        this.list = list;
    }

    public int getStartIndex() {
        return startIndex;
    }

    public void setStartIndex(int startIndex) {
        this.startIndex = startIndex;
    }
}
